void nForest(int n) {
	// Write your code here.

  //Number of lines is 'n'
	for(int i=0;i<n;i++)
	{
    //For each row, we have 'n' *'s
		for(int j=0;j<n;j++)
    {
			cout<<"* ";
    }
		cout<<endl;
	}
}

