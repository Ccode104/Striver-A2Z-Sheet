void nForest(int n) {
	// Write your code here.
	
	//Number of lines is 'n'
	for(int i=0;i<n;i++)
	{
		//Number of stars in a row is 'i+1'
		for(int j=0;j<=i;j++)
		{
			cout<<"* ";
		}
		cout<<endl;
	}
}

